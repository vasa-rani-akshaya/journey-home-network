const CategoryFilter = ({ onSelectCategory }: { onSelectCategory: (id: string) => void }) => {
  const [selectedCategory, setSelectedCategory] = useState("all");

  const handleCategorySelect = (categoryId: string) => {
    setSelectedCategory(categoryId);
    onSelectCategory(categoryId);
  };

  return (
    <div className="border-b pb-4">
      <ScrollArea className="w-full whitespace-nowrap">
        <div className="flex w-max space-x-6 px-4">
          {categories.map((category) => (
            <Button
              key={category.id}
              variant="ghost"
              size="sm"
              className={`flex flex-col items-center justify-center gap-1 rounded-none border-b-2 px-1 py-2 ${
                selectedCategory === category.id
                  ? "border-gray-800"
                  : "border-transparent hover:border-gray-200"
              }`}
              onClick={() => handleCategorySelect(category.id)}
            >
              <div className="h-6 w-6">{category.icon}</div>
              <span className="text-xs">{category.name}</span>
            </Button>
          ))}
        </div>
        <ScrollBar orientation="horizontal" />
      </ScrollArea>
    </div>
  );
};

export default CategoryFilter;
